# frozen_string_literal: true

require 'rails_helper'

RSpec.describe DragaoBasesController, type: :routing do
  describe 'routing' do
    it 'routes to #index' do
      expect(get: '/dragao_bases').to route_to('dragao_bases#index')
    end

    it 'routes to #new' do
      expect(get: '/dragao_bases/new').to route_to('dragao_bases#new')
    end

    it 'routes to #show' do
      expect(get: '/dragao_bases/1').to route_to('dragao_bases#show', id: '1')
    end

    it 'routes to #edit' do
      expect(get: '/dragao_bases/1/edit').to route_to('dragao_bases#edit', id: '1')
    end

    it 'routes to #create' do
      expect(post: '/dragao_bases').to route_to('dragao_bases#create')
    end

    it 'routes to #update' do
      expect(put: '/dragao_bases/1').to route_to('dragao_bases#update', id: '1')
    end

    it 'routes to #destroy' do
      expect(delete: '/dragao_bases/1').to route_to('dragao_bases#destroy', id: '1')
    end
  end
end

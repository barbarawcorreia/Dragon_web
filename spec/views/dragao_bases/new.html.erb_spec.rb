# frozen_string_literal: true

require 'rails_helper'

RSpec.describe "dragao_bases/new", :type => :view do
  before(:each) do
    assign(:dragao_base, DragaoBase.new(
      :name => 'MyString',
      :owner_name => 'MyString'
    ))
  end

  it 'renders new dragao_base form' do
    render

    assert_select 'form[action=?][method=?]', dragao_bases_path, 'post' do

      assert_select 'input#dragao_base_name[name=?]', 'dragao_base[name]'

      assert_select 'input#dragao_base_owner_name[name=?]', 'dragao_base[owner_name]'
    end
  end
end
